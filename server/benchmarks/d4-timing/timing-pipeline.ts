/**
 * D4 Timing Director — Pipeline Integration
 *
 * Wires D4 timing director into the production pipeline:
 *   1. Generates X-Sheet timing data for the episode (auto-generation from panel data)
 *   2. Applies per-user overrides if they exist (Wave 4 data model)
 *   3. Returns timing cues for downstream agents (D8 voice critic, D7 FX compositor)
 *
 * Called at Stage 12 (x_sheet) in the orchestrator, before voice_gen and fx_composite.
 * The X-Sheet provides:
 *   - Per-panel timing (duration, beat placement, transition timing)
 *   - Voice pacing cues (for D8 voice critic evaluation)
 *   - FX timing anchors (for D7 compositor start/duration fractions)
 *
 * The data model supports per-user overrides (Wave 4 schema) without migration:
 *   x_sheet_entries (auto-generated by D4) + x_sheet_overrides (user edits in Wave 5 UI)
 */
import {
  getPanelsByEpisode,
  getEpisodeById,
} from "../../db.js";
import { pipelineLog } from "../../observability/logger.js";
import {
  generateXSheet,
  type TimingDirectorOptions,
  type GeneratedXSheetResult,
} from "./timing-director.js";
import {
  getResolvedXSheet,
  getLatestXSheet,
  getXSheetEntries,
  createXSheet,
  createXSheetEntries,
  type ResolvedXSheet,
} from "./db-helpers.js";
import type { XSheetEntry, XSheetOverride } from "../../../drizzle/schema.js";

// ─── Types ──────────────────────────────────────────────────────────────────

export interface TimingPipelineOptions {
  /** Pipeline run ID */
  pipelineRunId: number;
  /** Episode ID */
  episodeId: number;
  /** User ID (for per-user overrides) */
  userId: number;
  /** Project ID */
  projectId: number;
  /** Force regeneration even if X-Sheet exists (default: false) */
  forceRegenerate?: boolean;
}

export interface TimingPipelineResult {
  /** Whether X-Sheet was generated fresh or loaded from cache */
  source: "generated" | "cached" | "cached_with_overrides";
  /** X-Sheet ID in database */
  xSheetId: number;
  /** Total entries in the X-Sheet */
  entryCount: number;
  /** Number of user overrides applied */
  overridesApplied: number;
  /** Final merged timing entries (D4 auto + user overrides) */
  entries: MergedTimingEntry[];
  /** Total episode duration in milliseconds (sum of all slice durations) */
  totalDurationMs: number;
  /** Cost in USD (only if generated fresh) */
  costUsd: number;
  /** Processing time in ms */
  durationMs: number;
}

export interface MergedTimingEntry {
  /** Entry ID from database */
  id: number;
  /** Panel ID reference */
  panelId: number | null;
  /** Slice number in timeline order */
  sliceNumber: number;
  /** Start time in episode timeline (ms) */
  startMs: number;
  /** End time in episode timeline (ms) */
  endMs: number;
  /** Duration of this slice (ms) */
  durationMs: number;
  /** Voice start offset within slice (ms) */
  voiceStartMs: number | null;
  /** Voice end offset within slice (ms) */
  voiceEndMs: number | null;
  /** Voice emotion directive for TTS */
  voiceEmotion: string | null;
  /** Whether this entry has user overrides */
  hasOverride: boolean;
}

// ─── Main Pipeline Integration ──────────────────────────────────────────────

/**
 * Run D4 Timing Director for an episode.
 * Generates X-Sheet if not cached, applies user overrides, and returns merged timing.
 *
 * Called by the orchestrator at Stage 12 (x_sheet), before voice_gen.
 * Downstream consumers:
 *   - D8 Voice Critic: uses voiceEmotion for evaluation context
 *   - D7 FX Compositor: uses timing anchors for effect placement
 *   - Assembly: uses durationMs and startMs for clip sequencing
 */
export async function runTimingPipeline(
  options: TimingPipelineOptions,
): Promise<TimingPipelineResult> {
  const {
    pipelineRunId,
    episodeId,
    userId,
    projectId,
    forceRegenerate = false,
  } = options;

  const startTime = Date.now();
  pipelineLog.info(`[D4 Pipeline] Starting timing director for episode ${episodeId}`);

  // Step 1: Check for existing resolved X-Sheet (with user overrides merged)
  if (!forceRegenerate) {
    const resolved = await getResolvedXSheet(episodeId, userId);
    if (resolved) {
      const entries = resolved.entries.map(e => toMergedEntry(e, resolved.hasOverrides));
      const totalDurationMs = entries.reduce((sum, e) => Math.max(sum, e.endMs), 0);
      const durationMs = Date.now() - startTime;

      pipelineLog.info(
        `[D4 Pipeline] Using cached X-Sheet #${resolved.sheet.id} ` +
        `(${entries.length} entries, overrides=${resolved.hasOverrides})`
      );

      return {
        source: resolved.hasOverrides ? "cached_with_overrides" : "cached",
        xSheetId: resolved.sheet.id,
        entryCount: entries.length,
        overridesApplied: resolved.hasOverrides ? entries.filter(e => e.hasOverride).length : 0,
        entries,
        totalDurationMs,
        costUsd: 0,
        durationMs,
      };
    }
  }

  // Step 2: Generate fresh X-Sheet from panel data
  const panels = await getPanelsByEpisode(episodeId);
  const episode = await getEpisodeById(episodeId);

  const panelInputs = panels.map(p => {
    const dialogue = p.dialogue as any;
    let dialogueArr: Array<{ character: string; text: string; emotion?: string }> = [];
    if (Array.isArray(dialogue)) {
      dialogueArr = dialogue.map((d: any) => ({
        character: d.character || d.speaker || "Unknown",
        text: d.text || d.line || String(d),
        emotion: d.emotion,
      }));
    } else if (dialogue && typeof dialogue === "object") {
      dialogueArr = [{
        character: dialogue.character || "Unknown",
        text: dialogue.text || dialogue.line || "",
        emotion: dialogue.emotion,
      }];
    }

    return {
      panelId: p.id,
      panelNumber: p.panelNumber,
      sceneNumber: p.sceneNumber,
      visualDescription: p.visualDescription || "",
      cameraAngle: p.cameraAngle || "medium",
      dialogue: dialogueArr,
      sfx: p.sfx || undefined,
      transition: p.transition || undefined,
      mood: (p as any).mood || "neutral",
    };
  });

  const timingOptions: TimingDirectorOptions = {
    episodeId,
    projectId,
    panels: panelInputs,
    targetDurationMs: (episode as any)?.targetDuration ? (episode as any).targetDuration * 1000 : undefined,
  };

  let genResult: GeneratedXSheetResult;
  try {
    genResult = await generateXSheet(timingOptions);
  } catch (err: any) {
    pipelineLog.error(`[D4 Pipeline] X-Sheet generation failed: ${err.message}`);
    throw new Error(`D4 Timing Director failed: ${err.message}`);
  }

  // Step 3: Load the generated entries
  const entries = await getXSheetEntries(genResult.xSheetId);
  const mergedEntries = entries.map(e => toMergedEntry(e, false));
  const totalDurationMs = genResult.totalDurationMs;

  const durationMs = Date.now() - startTime;
  pipelineLog.info(
    `[D4 Pipeline] Generated X-Sheet #${genResult.xSheetId}: ${entries.length} entries, ` +
    `total=${totalDurationMs}ms, confidence=${genResult.confidence}, ${durationMs}ms`
  );

  return {
    source: "generated",
    xSheetId: genResult.xSheetId,
    entryCount: entries.length,
    overridesApplied: 0,
    entries: mergedEntries,
    totalDurationMs,
    costUsd: 0.02, // LLM call cost for timing analysis
    durationMs,
  };
}

// ─── Helpers ────────────────────────────────────────────────────────────────

function toMergedEntry(entry: XSheetEntry, hasOverride: boolean): MergedTimingEntry {
  return {
    id: entry.id,
    panelId: entry.panelId,
    sliceNumber: entry.sliceNumber,
    startMs: entry.startMs,
    endMs: entry.endMs,
    durationMs: entry.durationMs,
    voiceStartMs: entry.voiceStartMs,
    voiceEndMs: entry.voiceEndMs,
    voiceEmotion: entry.voiceEmotion,
    hasOverride,
  };
}
